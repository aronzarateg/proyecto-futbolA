void listarPresidente(){
    listaPresidente =pd.listarPresidente();
    model = (DefaultTableModel) jdPRES.getModel();
        Object[] usu = new Object[12];
        for(int i=0;i<listaPresidente.size();i++){
            usu[0]=listaPresidente.get(i).getIdpresidente();
            usu[1]=listaPresidente.get(i).getIdcampeonato();
            usu[2]=listaPresidente.get(i).getIdequipo();
            usu[3]=listaPresidente.get(i).getNombre();
            usu[4]=listaPresidente.get(i).getPaterno();
            usu[5]=listaPresidente.get(i).getMaterno();
            usu[6]=listaPresidente.get(i).getDni();
            usu[7]=listaPresidente.get(i).getSexo();
            usu[8]=listaPresidente.get(i).getFechanaci();
            usu[9]=listaPresidente.get(i).getFechapresi();
            usu[10]=listaPresidente.get(i).getTelefono();
            model.addRow(usu);
               
        jdPRES.setModel(model);
        } 
}

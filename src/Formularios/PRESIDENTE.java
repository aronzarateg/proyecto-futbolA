    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
        // TODO add your handling code here:
         int fil = jdPRES.getSelectedRow();
        if(fil<0){
            JOptionPane.showMessageDialog(null, "Seleccionar El Presidente a MODIFICAR!");
        }else{
            int confirmar=JOptionPane.showConfirmDialog(null, "Esta seguro que desea MODIFICAR el Presidente? "); 
            if(JOptionPane.OK_OPTION==confirmar) {
                   int id = Integer.parseInt(txtCodigo.getText());
                   int idcam= Integer.parseInt(txtIDCampeonato.getText());
                   int idequi=Integer.parseInt(txtIDEquipo.getText());
                    String nombre = txtNombre.getText();
                    String paterno = txtPaterno.getText();
                    String materno = txtMaterno1.getText();
                    String DNI = txtDNI.getText();
                    
                    //new String(txtclave.getPassword());
                     String fechaNaci= ((JTextField)jFN.getDateEditor().getUiComponent()).getText() ;
                     String fechaDirec= ((JTextField)jANI.getDateEditor().getUiComponent()).getText() ;
                     String telefono = txtTelefono.getText();
                    int x = pd.modificarPresidente(id,idcam,idequi,nombre,paterno,materno,DNI,fechaNaci,fechaDirec,telefono);
                    if(x==1){
                        JOptionPane.showMessageDialog(null, "Presidente MODIFICADO!");
                        updateComponets();
                        limpiar();
                        btnEliminar.setEnabled(false);
                        btnModificar.setEnabled(false);
                        btnAgregar.setEnabled(false);
                        
                        inabilitar();
                    }else{
                        JOptionPane.showMessageDialog(null, "El Presidente no se ha MODIFICADO!");      
                    }                   
            }        
        }
    }//GEN-LAST:event_btnModificarActionPerformed


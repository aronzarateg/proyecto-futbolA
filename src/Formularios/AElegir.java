    private void btnSeguirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSeguirActionPerformed
       String usuario = lblusuario.getText();
        int confirmar=JOptionPane.showConfirmDialog(null,  "Esta seguro que desea Continuar y no crear otro campeonato\n si ya creo uno  solo continue "); 
        if(JOptionPane.OK_OPTION==confirmar) {
        MENU m = new MENU();
        m.setVisible(true);
        setVisible(false);
        MENU.lblusuario.setText(""+usuario);
        }else{
         JOptionPane.showMessageDialog(null,"");  
        }
    }//GEN-LAST:event_btnSeguirActionPerformed

